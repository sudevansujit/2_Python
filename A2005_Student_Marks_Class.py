score = int(input("Enter Student Score <= 100 : "))

if (score >=35) and (score < 50):
    print("You got IIIrd Class")
elif (score >=50) and (score < 60):
    print("You got IIrd Class")
elif (score >=60) and (score <85):
    print("You got Ist Class")
elif (score >=85) and (score <= 100):
    print("You got Distinction")
else:
    print("You Failed, Study Hard")

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
